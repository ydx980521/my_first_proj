
def add(a, b):
    """
    返回两个数的和

    参数:
    a (int 或 float): 第一个加数
    b (int 或 float): 第二个加数

    返回:
    int 或 float: 两个数的和
    """
    return a + b

# 测试函数
result = add(5, 3)
print("5 + 3 =", result)  # 输出: 5 + 3 = 8

result = add(2.5, 3.7)
print("2.5 + 3.7 =", result)  # 输出: 2.5 + 3.7 = 6.2
